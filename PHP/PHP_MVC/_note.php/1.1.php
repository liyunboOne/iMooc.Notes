>>>MVC全名是Model View Controller，
-模型（model）
-视图（view）
-控制器（controller）的缩写，
一种软件设计典范，用一种业务逻辑和数据显示分离的方法组织代码，将业务逻辑被聚焦到一个部件里面，在界面和用户围绕数据的交互能被改进和个性化定制的同时而不需要重新编写业务逻辑

>>>目的：
1、快速上手PHP框架，yii、thinkphp、ci等。
2、快速适应多数公司的web开发需求。
3、逐步、快速了解web系统架构的开发知识，为日后成为系统架构的中坚力量做准备。
>>>MVC解决的问题：
解决之前一步一步过程化的缓慢问题，快速搭建系统，高效和美工、设计方面的协作。使用MVC，有利于开发中的分工，更好地让代码高效重用。










